const random = Math.round(Math.random()*(5-1)+1);
switch (random) {
case 1:
        console.log("1 это твое число")
        break;
case 2:
        console.log("2 это твое число")
        break;
case 3:
        console.log("3 это твое число")
        break;
case 4:
        console.log("4 это твое число")
        break;
case 5:
        console.log("5 это твое число")
        break;
default:
        console.log("Тут нету твоего числа")
}